using UnityEngine;


public class TimeScale : MonoBehaviour
{
     /*************************************************************************************************
     *** Variables
     *************************************************************************************************/
     [SerializeField] float timeScale = 1f;


     /*************************************************************************************************
     *** Start
     *************************************************************************************************/
     void Start()
     {
          if (timeScale != 1f)
               Debug.LogWarning(string.Concat(gameObject.name, ": Time scale set to ", timeScale.ToString(), "."));

     }


     /*************************************************************************************************
     *** Update
     *************************************************************************************************/
     void Update()
     {
          if (timeScale != 1f)
               Time.timeScale = timeScale;

     }


     /*************************************************************************************************
     *** Properties
     *************************************************************************************************/



     /*************************************************************************************************
     *** Methods
     *************************************************************************************************/



}
